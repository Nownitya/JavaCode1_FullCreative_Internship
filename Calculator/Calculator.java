package calculator;

public class Calculator {
    public double addition(double a, double b) {

        return a + b;
    }

    public double subtractions(double a2, double b2) {
        return a2 - b2;
    }

    public double multiplication(double a3, double b3) {
        return a3 * b3;
    }

    public double division(double a4, double b4) {
        return a4 / b4;
    }


}